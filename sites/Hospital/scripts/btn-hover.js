const link_effect = document.querySelector("#link_effect");
link_effect.onmousemove = function(e) {
    const x = e.pageX - link_effect.offsetLeft;
    const y = e.pageY - link_effect.offsetTop;

    link_effect.style.setProperty("--x", x + "px");
    link_effect.style.setProperty("--y", y + "px");
}

